// TemplateRegistry 模板注册表
type TemplateRegistry struct {
	templates map[string]*template.Template
	mu        sync.RWMutex
}

// NewTemplateRegistry 创建模板注册表
func NewTemplateRegistry() *TemplateRegistry {
	return &TemplateRegistry{
		templates: make(map[string]*template.Template),
	}
}

// Register 注册模板
func (r *TemplateRegistry) Register(name, content string) error {
	r.mu.Lock()
	defer r.mu.Unlock()

	tmpl, err := template.New(name).Parse(content)
	if err != nil {
		return fmt.Errorf("模板解析失败: %w", err)
	}

	r.templates[name] = tmpl
	return nil
}

// Get 获取模板
func (r *TemplateRegistry) Get(name string) (*template.Template, error) {
	r.mu.RLock()
	defer r.mu.RUnlock()

	tmpl, ok := r.templates[name]
	if !ok {
		return nil, fmt.Errorf("模板不存在: %s", name)
	}

	return tmpl, nil
}

// List 列出模板
func (r *TemplateRegistry) List() []string {
	r.mu.RLock()
	defer r.mu.RUnlock()

	names := make([]string, 0, len(r.templates))
	for name := range r.templates {
		names = append(names, name)
	}
	return names
}

