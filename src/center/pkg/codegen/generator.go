// Generator 代码生成器
type Generator struct {
	config     GeneratorConfig
	templates  *TemplateRegistry
	formatters map[string]Formatter
	stats      *GeneratorStats
	mu         sync.RWMutex
}

// GeneratorConfig 生成器配置
type GeneratorConfig struct {
	OutputDir      string            `json:"output_dir"`
	TemplateDir    string            `json:"template_dir"`
	FileHeader     string            `json:"file_header"`
	FormatOnSave   bool              `json:"format_on_save"`
	Overwrite      bool              `json:"overwrite"`
	Variables      map[string]string `json:"variables"`
}

// DefaultGeneratorConfig 默认配置
func DefaultGeneratorConfig() GeneratorConfig {
	return GeneratorConfig{
		OutputDir:    "./generated",
		TemplateDir:  "./templates",
		FileHeader:   "// Code generated by OFA Codegen. DO NOT EDIT.",
		FormatOnSave: true,
		Overwrite:    false,
		Variables:    make(map[string]string),
	}
}

// GeneratorStats 生成器统计
type GeneratorStats struct {
	FilesGenerated int64 `json:"files_generated"`
	LinesGenerated int64 `json:"lines_generated"`
	TemplatesUsed  int64 `json:"templates_used"`
	Errors         int64 `json:"errors"`
}

// NewGenerator 创建生成器
func NewGenerator(config GeneratorConfig) *Generator {
	g := &Generator{
		config:     config,
		templates:  NewTemplateRegistry(),
		formatters: make(map[string]Formatter),
		stats:      &GeneratorStats{},
	}

	// 注册默认格式化器
	g.RegisterFormatter("go", &GoFormatter{})
	g.RegisterFormatter("ts", &TypeScriptFormatter{})
	g.RegisterFormatter("java", &JavaFormatter{})
	g.RegisterFormatter("py", &PythonFormatter{})
	g.RegisterFormatter("rs", &RustFormatter{})

	return g
}

// RegisterTemplate 注册模板
func (g *Generator) RegisterTemplate(name, content string) error {
	return g.templates.Register(name, content)
}

// RegisterFormatter 注册格式化器
func (g *Generator) RegisterFormatter(ext string, f Formatter) {
	g.formatters[ext] = f
}

// Generate 生成代码
func (g *Generator) Generate(templateName string, data interface{}, outputPath string) error {
	g.mu.Lock()
	defer g.mu.Unlock()

	// 检查文件是否存在
	if !g.config.Overwrite {
		if _, err := os.Stat(outputPath); err == nil {
			return fmt.Errorf("文件已存在: %s", outputPath)
		}
	}

	// 获取模板
	tmpl, err := g.templates.Get(templateName)
	if err != nil {
		return fmt.Errorf("模板不存在: %s", templateName)
	}

	// 合并变量
	mergedData := g.mergeData(data)

	// 执行模板
	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, mergedData); err != nil {
		g.stats.Errors++
		return fmt.Errorf("模板执行失败: %w", err)
	}

	content := buf.String()

	// 添加文件头
	if g.config.FileHeader != "" {
		ext := filepath.Ext(outputPath)
		content = g.addHeader(content, ext)
	}

	// 格式化
	if g.config.FormatOnSave {
		ext := strings.TrimPrefix(filepath.Ext(outputPath), ".")
		if f, ok := g.formatters[ext]; ok {
			formatted, err := f.Format(content)
			if err == nil {
				content = formatted
			}
		}
	}

	// 写入文件
	os.MkdirAll(filepath.Dir(outputPath), 0755)
	if err := os.WriteFile(outputPath, []byte(content), 0644); err != nil {
		g.stats.Errors++
		return fmt.Errorf("写入文件失败: %w", err)
	}

	g.stats.FilesGenerated++
	g.stats.LinesGenerated += int64(strings.Count(content, "\n"))
	g.stats.TemplatesUsed++

	return nil
}

// GenerateBatch 批量生成
func (g *Generator) GenerateBatch(tasks []GenerateTask) error {
	for _, task := range tasks {
		if err := g.Generate(task.Template, task.Data, task.Output); err != nil {
			return fmt.Errorf("生成 %s 失败: %w", task.Output, err)
		}
	}
	return nil
}

// mergeData 合并数据
func (g *Generator) mergeData(data interface{}) map[string]interface{} {
	result := make(map[string]interface{})

	// 添加全局变量
	for k, v := range g.config.Variables {
		result[k] = v
	}

	// 添加时间信息
	result["GeneratedAt"] = time.Now().Format(time.RFC3339)
	result["Year"] = time.Now().Year()

	// 合并传入数据
	if m, ok := data.(map[string]interface{}); ok {
		for k, v := range m {
			result[k] = v
		}
	}

	return result
}

// addHeader 添加文件头
func (g *Generator) addHeader(content, ext string) string {
	header := g.config.FileHeader

	switch ext {
	case ".go":
		return header + "\n\n" + content
	case ".ts", ".js":
		return "// " + header + "\n\n" + content
	case ".py":
		return "# " + header + "\n\n" + content
	case ".java":
		return "// " + header + "\n\n" + content
	case ".rs":
		return "// " + header + "\n\n" + content
	default:
		return header + "\n\n" + content
	}
}

