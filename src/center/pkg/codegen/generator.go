// InitDefaultTemplates 初始化默认模板
func (g *Generator) InitDefaultTemplates() error {
	for name, content := range DefaultTemplates {
		if err := g.RegisterTemplate(name, content); err != nil {
			return err
		}
	}
	return nil
}

// LoadTemplatesFromDir 从目录加载模板
func (g *Generator) LoadTemplatesFromDir(dir string) error {
	files, err := filepath.Glob(filepath.Join(dir, "*.tmpl"))
	if err != nil {
		return err
	}

	for _, file := range files {
		content, err := os.ReadFile(file)
		if err != nil {
			return err
		}

		name := strings.TrimSuffix(filepath.Base(file), ".tmpl")
		if err := g.RegisterTemplate(name, string(content)); err != nil {
			return err
		}
	}

	return nil
}

// Write 输出到Writer
func (g *Generator) Write(templateName string, data interface{}, w io.Writer) error {
	tmpl, err := g.templates.Get(templateName)
	if err != nil {
		return err
	}

	mergedData := g.mergeData(data)
	return tmpl.Execute(w, mergedData)
}